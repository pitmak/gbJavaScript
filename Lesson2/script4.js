/**
 * Возвращает сумму двух аргументов
 * @param {number} a первый аргумент
 * @param {number} b второй аргумент
 * @returns {number} a + b
 */
function add(a, b) {
    return a + b;
}

/**
 * Возвращает разность двух аргументов
 * @param {number} a первый аргумент
 * @param {number} b второй аргумент
 * @returns {number} a - b
 */
function sub(a, b) {
    return a - b;
}

/**
 * Возвращает произведение двух аргументов
 * @param {number} a первый аргумент
 * @param {number} b второй аргумент
 * @returns {number} a * b
 */
function mul(a, b) {
    return a * b;
}

/**
 * Возвращает частное двух аргументов
 * @param {number} a первый аргумент
 * @param {number} b второй аргумент
 * @returns {number} a / b
 */
function div(a, b) {
    return a / b;
}